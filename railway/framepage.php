	<p>Government of India is encouraging people to move towards a
		Cashless Economy. Cashless Economy can be achieved when the use of
		cash within the economy is gradually reduced and all transactions are
		made through electronic channels such as direct debit, credit and
		debit cards, electronic clearing, and payment systems such as
		Immediate Payment Service (IMPS), National Electronic Funds Transfer
		(NEFT) and Real Time Gross settlement (RTGS) in India.</p>
	<p>The advantages to citizens of a country in a Cashless Economy
		are:</p>
	<ul>
		<li>Cash need not be carried to avail goods and services. Risk of
			carrying currency notes and loss of hard earned money can be avoided.</li>
		<li>Extra payment on account of non-availability of change is
			avoided resulting in payment of what is actually due to be paid.
			Transaction costs are likely to be reduced.</li>
		<li>Convenience in shopping, payment of bills and scheduling of
			financial transactions can all be managed from home, office or
			wherever you are with a smart phone.</li>
		<li>It helps to authenticate financial transactions and maintain
			proper record. A record of all economic transactions through
			electronic means makes it almost impossible to sustain black market
			or underground economies that often proves damaging to national
			economy.</li>
		<li>Digital payments indirectly reduces expenditure in reducing
			need for printing of currency notes and its transportation.</li>
		<li>Electronic transactions helps in curbing corruption and black
			money flow, thereby helping in economic growth of the country.</li>
		<li>Lesser use of cash checks the grey economy, prevents money
			laundering and better tax compliance.</li>
		<li>Increased tax base would result in greater revenue for state
			and greater amount available to fund the welfare programmes of the
			Government.</li>
	</ul>
