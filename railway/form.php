<center><b>ABOUT IRCTC</b><br>

Indian Railway Catering and Tourism Corporation Ltd. has been set up by the Ministry of Railways with the basic purpose of hiving off entire catering and tourism activity of the railways to the new Corporation so as to professionalise and upgrade these services with public-private participation. Rail based Tourism in India will be the specific vehicle for achieving high growth in coordination with state agencies, tour operators, travel agents and the hospitality industry.<br>
<b>Mission</b>
<br>
"Enhance customer services and facilitation in railway catering, hospitality, travel and tourism with best industry practices".<br>
<b>Objectives</b><br>

(1) To be a customer friendly company through constant innovation, technology driven and human resource development.<br>

(2) Optimise resources, increase manpower productivity through quality product vending and innovative marketing strategies.<br>


(3) Evolve high standards of business ethics, quality management and effective cost control measures.<br>

(4) Concern for the environment and heritage.

